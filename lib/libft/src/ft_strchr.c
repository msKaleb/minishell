#include "ft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;
	int	l;

	if (!s)
		return (NULL);
	l = ft_strlen((char *)s);
	i = 0;
	while (i <= l)
	{
		if (s[i] == (unsigned char)c)
			return ((char *)s + i);
		i++;
	}
	return (NULL);
}
